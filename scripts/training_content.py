def get_training_content(topic):
    """获取培训内容"""
    return TRAINING_COURSES.get(topic, TRAINING_COURSES['overview'])

def get_quiz_scores(user_answers, topic):
    """计算测验得分"""
    course = TRAINING_COURSES.get(topic)
    if not course or 'quiz' not in course:
        return {'total': 0, 'correct': 0, 'score': 0, 'passed': False}
    quizzes = course['quiz']
    correct = sum(1 for i, a in enumerate(user_answers) if quizzes[i]['answer'] == a)
    total = len(quizzes)
    score = round(correct / total * 100) if total > 0 else 0
    return {
        'total': total,
        'correct': correct,
        'score': score,
        'passed': score >= 60,
        'results': [
            {
                'q': quizzes[i]['q'],
                'user_answer': user_answers[i],
                'correct': quizzes[i]['answer'],
                'is_correct': quizzes[i]['answer'] == user_answers[i],
                'explanation': quizzes[i]['explanation'],
            }
            for i in range(len(user_answers))
        ]
    }

